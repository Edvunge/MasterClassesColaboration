package javaBasico;

public class Variables {
    public static void main(String[] args) {
        // char, int, double, float, bool , (long , short)

        // inteiro
        int age = 20; // -2 -1 0 1 2
        // int peso = (int) 78.9; // conversao explicita

        // double / float
        double peso = 78.9;
        float altura = 1.90f;

        // char
        char letra = 'e';

        // boolean
        boolean vcEhAlto = true; // 1 ou 0 verdadeiro ou falso

        System.out.println("Peso: "+ peso);
        System.out.println("idade: " + age);
        System.out.println("altura: " + altura);
        System.out.println("letra: " + letra);
        System.out.println("voce eh alto: " + vcEhAlto);
    }
}
